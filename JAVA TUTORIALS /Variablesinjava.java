public class Variablesinjava {
    public static void main (String args []){
        int a = 10; 
        int b = 2;
        System.out.println(a);
        System.out.println(b);

        String name = "Ayush Gupta";
        System.out.println(name);

        a = b;
        System.out.println(a);
    }
}